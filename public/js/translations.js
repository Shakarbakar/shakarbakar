let translationObserver;
let isApplyingTranslations = false;

function getLanguage() {
  return localStorage.getItem(SHAKARBAKAR_LANGUAGE_KEY) || "en";
}

function setLanguage(language) {
  if (!["en", "fr", "ar"].includes(language)) {
    return;
  }

  localStorage.setItem(SHAKARBAKAR_LANGUAGE_KEY, language);
  applyTranslations();
}

function normalizeText(value) {
  return String(value || "").replace(/\s+/g, " ").trim();
}

function stripDecorativePrefix(value) {
  return normalizeText(value)
    .replace(/^[^\p{L}\p{N}]+/u, "")
    .replace(/[←→]+$/g, "")
    .trim();
}

function simplifyDecoratedText(value) {
  return stripDecorativePrefix(value)
    .replace(/→|→|←/g, " ")
    .replace(/•|•/g, " ")
    .replace(/[^\p{L}\p{N}?!.:,']+/gu, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function translateExact(value, language = getLanguage()) {
  const normalized = normalizeText(value);

  if (!normalized || language === "en") {
    return normalized;
  }

  const dictionary = uiTranslations[language] || {};
  const stripped = stripDecorativePrefix(normalized);
  const simplified = simplifyDecoratedText(normalized);

  if (dictionary[normalized]) {
    return dictionary[normalized];
  }

  if (dictionary[stripped]) {
    const prefix = normalized.slice(0, normalized.indexOf(stripped));
    const suffix = normalized.slice(normalized.indexOf(stripped) + stripped.length);
    return `${prefix}${dictionary[stripped]}${suffix}`.trim();
  }

  if (dictionary[simplified]) {
    return dictionary[simplified];
  }

  return normalized;
}

function translatePattern(value, language = getLanguage()) {
  const text = translateExact(value, language);

  if (language === "en") {
    return text;
  }

  const dictionary = uiTranslations[language] || {};
  const replacements = [
    [/^Score: (\d+)$/i, language === "ar" ? "النقاط: $1" : "Score : $1"],
    [/^Goals: (\d+) \| Streak: (\d+)$/i, language === "ar" ? "الأهداف: $1 | السلسلة: $2" : "Buts : $1 | Serie : $2"],
    [/^Find: (.+)$/i, language === "ar" ? "اعثر على: $1" : "Trouvez : $1"],
    [/^Showing puzzles (.+)$/i, language === "ar" ? "عرض الألغاز $1" : "Puzzles affiches $1"],
    [/^Correct! (.+)$/i, language === "ar" ? "صحيح! $1" : "Correct ! $1"],
    [/^Goal! Keeper dived (.+)\.$/i, language === "ar" ? "هدف! الحارس قفز نحو $1." : "But ! Le gardien a plonge vers $1."],
    [/^Missed! Keeper blocked (.+)\.$/i, language === "ar" ? "ضاعت! الحارس تصدى في $1." : "Rate ! Le gardien a bloque $1."],
    [/^(.+): higher or lower than (.+)'s (.+) titles\?$/i, language === "ar" ? "$1: أعلى أم أقل من ألقاب $2 وعددها $3؟" : "$1 : plus ou moins que les $3 titres de $2 ?"],
    [/^(.+) titles$/i, language === "ar" ? "$1 ألقاب" : "$1 titres"],
    [/^\? titles$/i, language === "ar" ? "؟ ألقاب" : "? titres"],
  ];

  for (const [pattern, replacement] of replacements) {
    if (pattern.test(text)) {
      return text.replace(pattern, replacement);
    }
  }

  return dictionary[text] || text;
}

function translateLegacyKey(key, language = getLanguage()) {
  const english = legacyKeyMap[key] || key;
  return translatePattern(english, language);
}

function shouldSkipNode(node) {
  if (!node || !node.parentElement) {
    return true;
  }

  return Boolean(
    node.parentElement.closest(
      "script, style, code, pre, textarea, [data-no-translate], .notranslate",
    ),
  );
}

function translateTextNode(node, language) {
  if (shouldSkipNode(node)) {
    return;
  }

  const original = node.__shakarbakarOriginalText || node.textContent;
  const normalized = normalizeText(original);

  if (!normalized) {
    return;
  }

  node.__shakarbakarOriginalText = original;
  const translated = translatePattern(original, language);

  if (translated !== normalizeText(node.textContent)) {
    const leading = String(original).match(/^\s*/)[0];
    const trailing = String(original).match(/\s*$/)[0];
    node.textContent = `${leading}${translated}${trailing}`;
  }
}

function translateElementAttributes(element, language) {
  if (!(element instanceof Element) || element.matches("script, style, [data-no-translate], .notranslate")) {
    return;
  }

  if (element.hasAttribute("data-translate")) {
    element.textContent = translateLegacyKey(element.getAttribute("data-translate"), language);
  }

  if (element.hasAttribute("data-translate-placeholder")) {
    element.placeholder = translateLegacyKey(
      element.getAttribute("data-translate-placeholder"),
      language,
    );
  }

  ["placeholder", "title", "aria-label"].forEach((attribute) => {
    if (!element.hasAttribute(attribute)) {
      return;
    }

    const originalAttribute = `data-original-${attribute}`;

    if (!element.hasAttribute(originalAttribute)) {
      element.setAttribute(originalAttribute, element.getAttribute(attribute));
    }

    element.setAttribute(
      attribute,
      translatePattern(element.getAttribute(originalAttribute), language),
    );
  });

  if (
    element instanceof HTMLInputElement &&
    ["button", "submit", "reset"].includes(element.type)
  ) {
    if (!element.dataset.originalValue) {
      element.dataset.originalValue = element.value;
    }

    element.value = translatePattern(element.dataset.originalValue, language);
  }
}

function translateTree(root = document.body, language = getLanguage()) {
  if (!root) {
    return;
  }

  if (root.nodeType === Node.TEXT_NODE) {
    translateTextNode(root, language);
    return;
  }

  if (root.nodeType !== Node.ELEMENT_NODE && root !== document) {
    return;
  }

  const walker = document.createTreeWalker(
    root,
    NodeFilter.SHOW_TEXT,
    {
      acceptNode(node) {
        return shouldSkipNode(node)
          ? NodeFilter.FILTER_REJECT
          : NodeFilter.FILTER_ACCEPT;
      },
    },
  );

  if (root instanceof Element) {
    translateElementAttributes(root, language);
    root.querySelectorAll("*").forEach((element) =>
      translateElementAttributes(element, language),
    );
  }

  let node = walker.nextNode();
  while (node) {
    translateTextNode(node, language);
    node = walker.nextNode();
  }
}

