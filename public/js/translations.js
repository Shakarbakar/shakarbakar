function createLanguageSelector() {
  const selector = document.createElement("div");
  selector.className = "language-selector";
  selector.setAttribute("aria-label", "Language selector");

  Object.entries(languageLabels).forEach(([language, label]) => {
    const button = document.createElement("button");
    button.type = "button";
    button.dataset.language = language;
    button.textContent = label;
    button.addEventListener("click", () => setLanguage(language));
    selector.appendChild(button);
  });

  return selector;
}

function ensureLanguageSelector() {
  const languageContainers = document.querySelectorAll(".languages");

  if (languageContainers.length > 0) {
    languageContainers.forEach((container) => {
      if (!container.querySelector(".language-selector")) {
        container.replaceChildren(createLanguageSelector());
      }
    });
  } else if (!document.querySelector(".language-selector")) {
    const floating = createLanguageSelector();
    floating.classList.add("language-selector-floating");
    document.body.appendChild(floating);
  }

  const language = getLanguage();
  document.querySelectorAll(".language-selector button").forEach((button) => {
    button.classList.toggle("active", button.dataset.language === language);
    button.setAttribute(
      "aria-pressed",
      String(button.dataset.language === language),
    );
  });
}

function applyDocumentDirection(language) {
  document.documentElement.lang = language;
  document.documentElement.dir = language === "ar" ? "rtl" : "ltr";
  document.body.classList.toggle("rtl-language", language === "ar");
}

function applyTranslations() {
  const language = getLanguage();

  isApplyingTranslations = true;
  if (translationObserver) {
    translationObserver.disconnect();
  }

  ensureLanguageStyles();
  applyDocumentDirection(language);
  ensureLanguageSelector();
  translateTree(document.body, language);

  if (translationObserver) {
    translationObserver.observe(document.body, {
      childList: true,
      subtree: true,
      characterData: true,
      attributes: true,
      attributeFilter: ["placeholder", "title", "aria-label", "value"],
    });
  }

  isApplyingTranslations = false;
}

function installTranslatedAlert() {
  if (window.__shakarbakarAlertTranslated) {
    return;
  }

  const nativeAlert = window.alert.bind(window);
  window.alert = (message) => {
    nativeAlert(translatePattern(message, getLanguage()));
  };
  window.__shakarbakarAlertTranslated = true;
}

function startTranslationObserver() {
  if (translationObserver || !document.body) {
    return;
  }

  translationObserver = new MutationObserver((mutations) => {
    if (isApplyingTranslations) {
      return;
    }

    const language = getLanguage();
    isApplyingTranslations = true;
    translationObserver.disconnect();

    mutations.forEach((mutation) => {
      if (mutation.type === "characterData") {
        translateTextNode(mutation.target, language);
      }

      if (mutation.type === "attributes") {
        translateElementAttributes(mutation.target, language);
      }

      mutation.addedNodes.forEach((node) => translateTree(node, language));
    });

    ensureLanguageSelector();
    translationObserver.observe(document.body, {
      childList: true,
      subtree: true,
      characterData: true,
      attributes: true,
      attributeFilter: ["placeholder", "title", "aria-label", "value"],
    });

    isApplyingTranslations = false;
  });

  translationObserver.observe(document.body, {
    childList: true,
    subtree: true,
    characterData: true,
    attributes: true,
    attributeFilter: ["placeholder", "title", "aria-label", "value"],
  });
}

document.addEventListener("DOMContentLoaded", () => {
  installTranslatedAlert();
  startTranslationObserver();
  applyTranslations();
});

window.getLanguage = getLanguage;
window.setLanguage = setLanguage;
window.applyTranslations = applyTranslations;
